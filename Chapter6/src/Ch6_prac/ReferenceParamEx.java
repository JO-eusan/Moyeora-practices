package Ch6_prac;

// class Data {int x;}

public class ReferenceParamEx {

	public static void main(String[] args) {
		Data d = new Data();
		d.x = 10; // 값 초기화
		System.out.println("main() : x = " + d.x);
		
		change(d);
		System.out.println("After change(d)");
		System.out.println("main() : x = "+ d.x);

	}
	
	static void change(Data d) { // 참조형 매개변수
		d.x = 1000; // 주소값 자체를 변경
		System.out.println("change() : x = " + d.x);
	}
}
