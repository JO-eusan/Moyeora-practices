package Ch2_prac;

public class SpecialCharEx {

	public static void main(String[] args) {
		System.out.println('\''); //작은따옴표
		System.out.println("abc\t123\b456"); // tap&backspace
		System.out.println('\n'); // 줄바꿈
		System.out.println("\"Hello\""); //큰따옴표
		System.out.println("c:\\"); // 역슬래쉬
	}

}
